#ifndef __MATH_H_
#define __MATH_H_

/**
 * @brief Return absolute value of given int
 * 
 * @param val 
 * @return int 
 */
int abs(int val);

/**
 * @brief Return absolute value of given double
 * 
 * @param val 
 * @return double 
 */
double absd(double val);

/**
 * @brief Return absolute value of given float
 * 
 * @param val 
 * @return float 
 */
float absf(float val);

#endif
